package identity;

public class Staff extends Person {

    private String role;

    public Staff(String name, String role) {
        super(name);
        this.role = role;
    }

    public String getRole() {
        return role;
    }

    public void setRole(String role) {
        this.role = role;
    }

    @Override
    public String getRoleDescription() {
        return "Cinema staff: " + role;
    }

}
